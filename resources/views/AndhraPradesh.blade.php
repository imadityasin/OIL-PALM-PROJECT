    <h1>Andhra Pradesh</h1>
    <div class="Andharcontain">
        <div class="AndharMdiv">
            <p class="AndharHeadPTag">Andhra Pradesh stands 1st in Area and Production of Oil Palm in the Country with an area of 1.62 lakh ha and 14.09
                Lakh MT of FFBs (1.14 lakh Farmers) with an average productivity of 19.81 Tons per ha from the bearing plantations. Oil Palm programme
                is being taking in the state, duly allotting the oil Palm factory zones to the Oil Processing units. The factories also have to procure Oil
                Palm Fresh Fruit Bunches (FFBs) from the Oil Palm farmers and also to pay the price as fixed by the Government adhering to the Oil Palm
                Act 1993 and AP Oil Palm (Regulation of Production and Processing) Rules 2008.
            </p>
            <img src="./assets/images/AndhraPradeshImg.png" alt="Andhra Pradesh" class="AndhraPradeshImg">
            <div class="AndharkeyDiv">
                <div class="AndharImgkeyDiv"></div>
                <p class="AndharImgKeyPTag">
                    Oil Palm cultivation supported districts
                </p>
            </div>
            <div class="paraDiv">
                <h4 class="AndharH4Tag">Geography</h4>
                <p class="AndharDesPTag">
                    Andhra Pradesh lies between 12°41' and 19.07°N latitude and 77° and 84°40'E longitude, and is bordered by Telangana to the north and west, Chhattisgarh to the north-west, Orissa to the north, the Bay of Bengal to the east, Tamil Nadu to the south and Karnataka to the southwest and west. The climate of Andhra Pradesh state is generally hot and humid, suitable for Oil Palm cultivation. Two major rivers, the Godavari and the Krishna run across the state.
                </p>
            </div>
            <div class="paraDiv">
                <h4 class=" AndharH4Tag">Soil Type</h4>
                <p class="AndharDesPTag">
                    Generally, oil palms can be grown on wide range of soils. They thrive best in moist deep, loamy soils, rich in humus with good water permeability and Soil pH of 4 – 6.
                    <br>
                    Andhra Pradesh is endowed with a variety of soils ranging from poor coastal sands to highly fertile deltaic alluviums.
                    <br>
                    Red Soils occupy over 66% of the cultivated area whereas 25% of the cultivated area is covered by Black soil.
                </p>
            </div>
            <div class="paraDiv">
                <h4 class="AndharH4Tag">Vegetation</h4>
                <p class="AndharDesPTag">
                    Andhra Pradesh has a varied topography ranging from the hills of Eastern Ghats and Nallamallas to the shores of Bay of Bengal supports varied ecotypes, which in turn support a rich diversity of flora. The state of Andhra Pradesh is considered one of the rich biodiversity states in India. In Andhra Pradesh, vegetation that is seen usually belongs to dry deciduous form of vegetation that is a mix of Teak and a variety of species belonging to genera Dalbergias, Terminalias, Anogeissus, Pterocarpus, etc. Mangroves form an important eco-system in East Godavari, Nellore, Krishna, and Guntur districts. Andhra Pradesh has about 3188.74 ha. of mangrove forests.
                </p>
            </div>
            <div class="paraDiv">
                <h4 class="AndharH4Tag">Climatic Conditions</h4>
                <p class="AndharDesPTag">
                    The climate of Andhra Pradesh is generally hot and humid in the lowland coastal regions, while it is mostly semi-arid in parts of Anantapur district, Kurnool district and Kadapa district. These areas fall under the rain shadow region of the Western Ghats.
                    <br>
                    The summer season in this state generally extends from March to May or June.
                    <br>
                    Monsoon season starts in May or June and continues till October. The major role in determining the climate of the state is played by southwest monsoon. About one third of the total rainfall in Andhra Pradesh is brought by the northeast monsoons around the month of October.
                    <br>
                    The lowest rainfall occurs in Anantapur district, at 311 mm per year, while the highest occurs Visakhapatnam district at 738 mm.
                    <br>
                    October to February are the winter months in Andhra Pradesh.The temperature in winter ranges from around 13 °C to 30 °C in coastal areas and from about 15°C to 36°C in the rain shadow.
                </p>
            </div>

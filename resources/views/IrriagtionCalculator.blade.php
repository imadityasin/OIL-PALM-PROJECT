                    <div class="IrrigaCalDesSpanTag">
                        <p style="text-indent: 32px;margin-bottom:15px;">The following simple method of calculation has been devised based on the evaporting rates prevaling in the area especially during summer months:
                            <br>
                        </p>
                        <p style="margin-left:32px;line-height:2rem">
                            Evaporation from open pan:6.70mm
                            <br>
                            Crop Factor : 0.7
                            <br>
                            Potential evapotranspiration (PE) = Pan evaporation x Crop Factor
                            <br>
                            = 6.70 x 0.7 (Crop factor being assumed as 0.7)
                            <br>
                            = 4.69 mm/day
                            <br>
                            = 46,900 l/day/ha (1 mm of rainfall is equal to 1 l/sq.m)
                        </p>
                    </div>
                </div>
                <div class="IrrigaCalDesRowDiv">
                    <div class="IrrigaCalDesSpanTag">
                        <p style="text-indent: 32px;margin-bottom:15px;">
                            Since 143 plams are accommodated in one hector area,
                            the quantity of water per plam day workd out to be 328 l.
                        </p>
                        <p style="text-indent: 32px;margin-bottom:15px;">
                            Water holding capacity at not less than 70% of the feild capacity is
                            acceptable and will not affect the FEB yeild of oil plam significantly.
                        </p>
                        <p style="text-indent: 32px;margin-bottom:15px;">
                            Therefore, the minimum quantity of water to be applied will be:
                        </p>
                        <p style="text-indent: 32px;margin-bottom:15px;">
                            4.69 mm x 70% = 3.283 mm/day or 32,830 l/ha/day or 230 l/plam/day
                        </p>
                        <p style="text-indent: 32px">
                            By providing irriagtion regularly at the above quantities coupled with
                            proper nutrient management, FFB yield of 20 tonnes/ha/year could be ensured.
